"""Strategy Execution Engine for TraderClaw.

This module provides the core strategy execution capabilities:
- Plain language strategy interpretation via LLM
- Technical indicator integration
- Multi-model consensus (DeepSeek + Qwen)
- Hybrid approval flow (auto-execute vs ask user)
- Multi-account support for strategy comparison
"""

